#include "../../includes/libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	digit;
	long	nb;

	nb = n;
	if (nb == -2147483648)
	{
		ft_putchar_fd('-', fd);
		ft_putchar_fd('2', fd);
		nb = 147483648;
	}
	if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr_fd(nb / 10, fd);
	}
	digit = nb % 10 + '0';
	ft_putchar_fd(digit, fd);
}
